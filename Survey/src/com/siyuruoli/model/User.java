package com.siyuruoli.model;
/**
 * 用户表
 * @author biao
 *
 */
public class User {

	private Integer userId;
	private String userNickName;
	private String userAccount;
	private String userPassword;
	private String sex;
	private String userPhone;
	private String userSalary;
	private String education;
	private String signature;
	private String likeMusic;
	private String wish;
	private String userType;
	private String isDestroy;

	



	public Integer getUserId() {
		return userId;
	}
	public void setUserId(Integer userId) {
		this.userId = userId;
	}
	public String getUserNickName() {
		return userNickName;
	}
	public void setUserNickName(String userNickName) {
		this.userNickName = userNickName;
	}
	public String getUserAccount() {
		return userAccount;
	}
	public void setUserAccount(String userAccount) {
		this.userAccount = userAccount;
	}
	public String getUserPassword() {
		return userPassword;
	}
	public void setUserPassword(String userPassword) {
		this.userPassword = userPassword;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	
	public String getUserPhone() {
		return userPhone;
	}
	public void setUserPhone(String userPhone) {
		this.userPhone = userPhone;
	}
	public String getUserSalary() {
		return userSalary;
	}
	public void setUserSalary(String userSalary) {
		this.userSalary = userSalary;
	}
	public String getEducation() {
		return education;
	}
	public void setEducation(String education) {
		this.education = education;
	}
	public String getSignature() {
		return signature;
	}
	public void setSignature(String signature) {
		this.signature = signature;
	}
	public String getLikeMusic() {
		return likeMusic;
	}
	public void setLikeMusic(String likeMusic) {
		this.likeMusic = likeMusic;
	}
	public String getWish() {
		return wish;
	}
	public void setWish(String wish) {
		this.wish = wish;
	}
	public String getUserType() {
		return userType;
	}
	public void setUserType(String userType) {
		this.userType = userType;
	}
	public String getIsDestroy() {
		return isDestroy;
	}
	public void setIsDestroy(String isDestroy) {
		this.isDestroy = isDestroy;
	}
	
	

	
}
